

public class PreetyPrinting {
    public static void main(String[] args) {
        float a=1231132.12745f;
        System.out.printf("Main stream: %.4f",a);//it will also round off last value
        System.out.printf("Pie: %.3f",Math.PI); 

        System.out.printf("hello my name is %s & i am %s", "aryan","cool");
    }
}
/*There are many format specifiers we can use. Here are some common ones:

%c - Character
%d - Decimal number (base 10)
%e - Exponential floating-point number
%f - Floating-point number
%i - Integer (base 10)
%o - Octal number (base 8)
%s - String
%u - Unsigned decimal (integer) number
%x - Hexadecimal number (base 16)
%t - Date/time
%n - Newline
*/